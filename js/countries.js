// Helper functions
function getCountriesByContinent(continent) {
    return countries.filter(country => country.continent === continent);
}

function getUniqueContinents() {
    return [...new Set(countries.map(country => country.continent))];
}

function getCountryByName(name) {
    return countries.find(country => country.name.toLowerCase() === name.toLowerCase());
}

function getRandomCountry(countryList = countries) {
    const randomIndex = Math.floor(Math.random() * countryList.length);
    return countryList[randomIndex];
}

function getCountrySuggestions(input) {
    if (!input) return [];
    const lowerInput = input.toLowerCase();
    return countries
        .filter(country => country.name.toLowerCase().startsWith(lowerInput))
        .map(country => country.name)
        .slice(0, 5);
} 